
function putOn()
  {
  for (i = 0; i < document.forms[0].elements.length; i++)
    {
    document.forms[0].elements[i].checked = true;
    }
  }
function putOff()
  {
  for (i = 0; i < document.forms[0].elements.length; i++)
    {
    document.forms[0].elements[i].checked = false;
    }
  }

